package hellopackage;

public class Exercise3 {
//***********数据类型的区别
	
	public static void main(String[] args) {
		int x = 10;
		int y = 3;
		System.out.println("10除以3结果是:     "+x/y);
		
		double a = 10;
		double b = 3;
		System.out.println("10除以3结果是:     "+a/b);
	
		float c = 10;
		float d = 3;
		System.out.println("10除以3结果是:     "+c/d);
			
		
	}

}
